public class Exercicio3_6 {
    public static void main(String args[]){

        // Calculando e mostrando o volume de um corpo a partir do valor de três variáveis.
        int altura = 175;
        int largura = 81;
        int profund = 38;
        int volume = altura * largura * profund;
        System.out.println("O volume deste corpo é definido por: Altura = " + altura + ", Largura = " + largura + " e Profundidade = " + profund + ".");
        System.out.println("O volume é igual a: " + volume + ".");
    }
}